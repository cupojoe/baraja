function applyLang(lang) {
  const t = translations[lang];
  document.documentElement.lang = lang;
  document.title = t.page_title;
  document.querySelector('meta[name="description"]').setAttribute("content", t.page_desc);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
    btn.setAttribute("aria-pressed", String(btn.dataset.lang === lang));
  });

  try {
    localStorage.setItem("lang", lang);
  } catch (_) {}
}

// Tabs
const tabGroups = document.querySelectorAll("[data-tabs]");

tabGroups.forEach((group) => {
  const buttons = group.querySelectorAll("[data-tab]");
  const panels = group.querySelectorAll("[data-panel]");

  buttons.forEach((button) => {
    button.addEventListener("click", () => {
      const selected = button.dataset.tab;

      buttons.forEach((item) => {
        const isActive = item === button;
        item.classList.toggle("is-active", isActive);
        item.setAttribute("aria-selected", String(isActive));
      });

      panels.forEach((panel) => {
        panel.classList.toggle("is-active", panel.dataset.panel === selected);
      });
    });
  });
});

// Language toggle
document.querySelectorAll(".lang-btn").forEach((btn) => {
  btn.addEventListener("click", () => applyLang(btn.dataset.lang));
});

// Init — prefer saved preference, then browser language, then English
let initLang = "en";
try {
  const saved = localStorage.getItem("lang");
  if (saved && translations[saved]) {
    initLang = saved;
  } else if (navigator.language.startsWith("es")) {
    initLang = "es";
  }
} catch (_) {}

applyLang(initLang);
